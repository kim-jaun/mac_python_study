a = 3; b = 4 # 두즐을 하나로 합할 때
print('a+b =',a+b)
print('a-b =',a-b)
print('a*b =',a*b)
print('a/b =',a/b)  # 나눗셈의 결과는 실수
print('a**b =',a**b) # ** 거듭제곱
print('b%a =',b%a)   # %는 나머지
print('b//a=',b//a)  # //s는 몫
print('2 + 3 * 2 =',2 + 3*2)     # *, /, //, %는 +,-보다 먼저 실행
print('(2 + 3) * 2=',(2 + 3)*2)  # 괄호가 있으면 괄호를 먼저 실행